package org.fightteam.join.auth.web.controller;

import org.fightteam.join.auth.data.models.User;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.data.rest.webmvc.json.JsonSchema;
import org.springframework.data.rest.webmvc.json.PersistentEntityToJsonSchemaConverter;
import org.springframework.hateoas.ExposesResourceFor;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.ResponseBody;
import org.springframework.web.bind.annotation.RestController;

/**
 * [description]
 *
 * @author faith
 * @since 0.0.1
 */
@Controller
@ExposesResourceFor(User.class)
@RequestMapping("/users")
public class UserController {
    @Autowired
    private PersistentEntityToJsonSchemaConverter jsonSchemaConverter;

    @RequestMapping(value = "/schema", method = RequestMethod.GET)
    @ResponseBody
    public JsonSchema schema() {
        return jsonSchemaConverter.convert(User.class);
    }
}
